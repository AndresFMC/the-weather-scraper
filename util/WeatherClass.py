class Weather:
    '''Class for storing Wunderground Weather Data Rows'''
    def __init__(self, date_time, temperature, dew_point, humidity, wind_speed, gust, pressure, precipitation_rate, precipitation_accumulation, uv, solar):
        date_time = date_time
        temperature = temperature
        dew_point = dew_point
        humidity = humidity
        wind_speed = wind_speed
        gust = gust
        pressure = pressure
        precipitation_rate = precipitation_rate
        precipitation_accumulation = precipitation_accumulation
        uv = uv
        solar = solar